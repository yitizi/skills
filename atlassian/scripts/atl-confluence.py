# ====== Subcommand: get-page-history ======


def cmd_get_page_history(args: argparse.Namespace) -> int:
    data = call_api_json(
        "confluence",
        f"rest/api/content/{args.page_id}/version?limit={args.limit}",
    )
    results = data.get("results", []) if data else []
    if args.json:
        print(json.dumps(data, ensure_ascii=False, indent=2))
        return 0
    print(f"{'VER':<5} {'WHEN':<22} {'WHO':<20} {'MSG':<50}")
    print("-" * 100)
    for r in results:
        by = r.get("by", {})
        username = by.get("username") or by.get("displayName", "?")
        print(f"v{r.get('number', '?'):<4} {r.get('when', '?')[:22]:<22} {username:<20} {(r.get('message') or '')[:50]}")
    return 0


# ====== Subcommand: get-page-diff (between two remote versions) ======


def cmd_get_page_diff(args: argparse.Namespace) -> int:
    """Fetch v_from and v_to body.storage and unified-diff them."""
    def fetch_version(v):
        return call_api_json(
            "confluence",
            f"rest/api/content/{args.page_id}?expand=body.storage,version&version={v}",
        )

    a = fetch_version(args.from_version)
    b = fetch_version(args.to_version)
    a_body = (a.get("body", {}).get("storage", {}) or {}).get("value", "").splitlines(keepends=True)
    b_body = (b.get("body", {}).get("storage", {}) or {}).get("value", "").splitlines(keepends=True)
    diff = list(difflib.unified_diff(
        a_body, b_body,
        fromfile=f"v{args.from_version}",
        tofile=f"v{args.to_version}",
    ))
    if not diff:
        print(f"No changes between v{args.from_version} and v{args.to_version}")
        return 0
    sys.stdout.writelines(diff)
    return 0


# ====== Subcommand: get-comments ======


def cmd_get_comments(args: argparse.Namespace) -> int:
    data = call_api_json(
        "confluence",
        f"rest/api/content/{args.page_id}/child/comment?expand=body.view,version&limit={args.limit}",
    )
    results = data.get("results", []) if data else []
    if args.json:
        print(json.dumps(data, ensure_ascii=False, indent=2))
        return 0
    for r in results:
        ver = r.get("version", {})
        body = (r.get("body", {}).get("view", {}) or {}).get("value", "")
        # strip HTML tags for terminal display
        text = re.sub(r"<[^>]+>", "", body).strip()
        print(f"--- comment id={r.get('id')} by {ver.get('by',{}).get('displayName','?')} at {ver.get('when','?')[:19]} ---")
        print(text[:500])
        print()
    print(f"Total: {len(results)}")
    return 0


# ====== Subcommand: add-comment ======


def cmd_add_comment(args: argparse.Namespace) -> int:
    if args.file:
        body_html = Path(args.file).read_text(encoding="utf-8")
    elif args.text:
        # Wrap plain text in storage <p>
        body_html = f"<p>{_html_escape(args.text)}</p>"
    else:
        raise SystemExit("provide --text or --file")

    payload = {
        "type": "comment",
        "container": {"id": args.page_id, "type": "page"},
        "body": {"storage": {"value": body_html, "representation": "storage"}},
    }
    tmp = tempfile.NamedTemporaryFile(suffix=".json", mode="w", encoding="utf-8", delete=False)
    try:
        json.dump(payload, tmp, ensure_ascii=False)
        tmp.close()
        rc, stdout, stderr = call_api("confluence", "rest/api/content", method="POST", body_file=tmp.name)
        if rc != 0:
            print(f"ERROR: {stderr}\n{stdout}", file=sys.stderr)
            return 1
    finally:
        os.unlink(tmp.name)
    text = stdout.strip()
    if text.startswith("﻿"):
        text = text[1:]
    data = json.loads(text)
    print(f"OK comment id={data.get('id')} added to page {args.page_id}")
    return 0


def _html_escape(s: str) -> str:
    return (s.replace("&", "&amp;")
             .replace("<", "&lt;")
             .replace(">", "&gt;"))


# ====== Subcommand: delete-page ======


def cmd_delete_page(args: argparse.Namespace) -> int:
    if not args.yes:
        print(f"Refusing to delete page {args.page_id} without --yes", file=sys.stderr)
        return 1
    rc, stdout, stderr = call_api("confluence", f"rest/api/content/{args.page_id}", method="DELETE")
    if rc != 0:
        print(f"ERROR: {stderr}\n{stdout}", file=sys.stderr)
        return 1
    print(f"OK deleted page {args.page_id}")
    return 0


# ====== Subcommand: move-page ======


def cmd_move_page(args: argparse.Namespace) -> int:
    # Fetch current to preserve title/version AND body.
    # Confluence PUT replaces the entire page representation; if `body` is omitted,
    # some server versions return 400, others silently wipe content. Always include
    # body.storage to be safe.
    cur = call_api_json(
        "confluence",
        f"rest/api/content/{args.page_id}?expand=version,title,space,body.storage",
    )
    body_value = ((cur.get("body") or {}).get("storage") or {}).get("value", "")
    payload = {
        "version": {"number": cur["version"]["number"] + 1},
        "title": cur["title"],
        "type": "page",
        "ancestors": [{"id": args.parent}],
        "body": {"storage": {"value": body_value, "representation": "storage"}},
    }
    tmp = tempfile.NamedTemporaryFile(suffix=".json", mode="w", encoding="utf-8", delete=False)
    try:
        json.dump(payload, tmp, ensure_ascii=False)
        tmp.close()
        rc, stdout, stderr = call_api(
            "confluence", f"rest/api/content/{args.page_id}",
            method="PUT", body_file=tmp.name,
        )
        if rc != 0:
            print(f"ERROR: {stderr}\n{stdout}", file=sys.stderr)
            return 1
    finally:
        os.unlink(tmp.name)
    print(f"OK moved page {args.page_id} under parent {args.parent}")
    return 0


# ====== Subcommand: upload-attachment ======


def cmd_upload_attachment(args: argparse.Namespace) -> int:
    src = Path(args.file).resolve()
    if not src.exists():
        raise SystemExit(f"File not found: {src}")
    # Confluence requires multipart upload with X-Atlassian-Token: no-check
    # Easiest via curl directly (atl-query.ps1 is JSON-oriented).
    # We invoke curl with --netrc here.
    config = _read_config()
    base = config.get("CONFLUENCE_URL", "")
    if not base:
        raise SystemExit("CONFLUENCE_URL not configured")
    url = f"{base.rstrip('/')}/rest/api/content/{args.page_id}/child/attachment"

    cmd = [
        "curl", "-s", "--netrc",
        "-H", "X-Atlassian-Token: no-check",
        "-F", f"file=@{src};filename={src.name}",
        "-X", "POST", url,
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8", errors="replace")
    if proc.returncode != 0:
        print(f"ERROR: {proc.stderr}\n{proc.stdout}", file=sys.stderr)
        return 1
    print(proc.stdout)
    return 0


# ====== Subcommand: download-attachment ======


def cmd_download_attachment(args: argparse.Namespace) -> int:
    # List attachments to find the matching one
    data = call_api_json(
        "confluence",
        f"rest/api/content/{args.page_id}/child/attachment?expand=version&limit=200",
    )
    target = None
    for r in (data or {}).get("results", []):
        if r.get("title") == args.filename:
            target = r
            break
    if not target:
        print(f"No attachment named '{args.filename}' on page {args.page_id}", file=sys.stderr)
        return 1

    download_link = target.get("_links", {}).get("download", "")
    if not download_link:
        # Fallback: build from id
        download_link = f"/download/attachments/{args.page_id}/{args.filename}"

    config = _read_config()
    base = config.get("CONFLUENCE_URL", "")
    url = f"{base.rstrip('/')}{download_link}"

    out = Path(args.out) if args.out else Path(args.filename)
    # Run curl in binary mode (no text decoding) to handle non-ASCII filenames safely.
    # curl writes the binary body directly via -o; we only need exit code + stderr.
    # Decode stderr explicitly as UTF-8 with error replacement to avoid cp936 surprises.
    cmd = ["curl", "-s", "-S", "--netrc", "-L", "-o", str(out), "-w", "%{http_code}", url]
    proc = subprocess.run(cmd, capture_output=True)  # bytes, not text
    if proc.returncode != 0:
        err = proc.stderr.decode("utf-8", errors="replace")
        print(f"ERROR curl exit {proc.returncode}: {err}", file=sys.stderr)
        return 1
    http_code = proc.stdout.decode("ascii", errors="replace").strip()
    if http_code.startswith(("4", "5")):
        # curl wrote (likely error JSON) into the output file; read first 500 bytes for diagnosis
        snippet = ""
        if out.exists():
            try:
                snippet = out.read_bytes()[:500].decode("utf-8", errors="replace")
            except Exception:
                pass
        print(f"ERROR HTTP {http_code} downloading '{args.filename}'. Response snippet: {snippet}",
              file=sys.stderr)
        return 1
    size = out.stat().st_size if out.exists() else 0
    print(f"OK downloaded -> {out} ({size} bytes)")
    return 0


def _read_config() -> dict:
    cfg = Path.home() / ".claude" / "skill-config" / "atlassian" / "config.env"
    out: dict[str, str] = {}
    if not cfg.exists():
        return out
    for line in cfg.read_text(encoding="utf-8", errors="replace").splitlines():
        m = re.match(r"^([A-Z_]+)=(.+)$", line.strip())
        if m:
            out[m.group(1)] = m.group(2).strip()
    return out


